package be.Bajeux.demos;

public class DemoOperateur {

    public static void main(String[] args) {

        int nb1 = 5, nb2 =3 ,result;
        // add
        result = nb1 + nb2;
        System.out.println(nb1 + " + " + nb2 + " = " + result);
        //soutraction
        result = nb1 - nb2;
        System.out.println(nb1 + " - " + nb2 + " = " + result);
        //multiplication
        result = nb1 * nb2;
        System.out.println(nb1 + " * " + nb2 + " = " + result);
        //division
        result = nb1 / nb2;
        System.out.println(nb1 + " / " + nb2 + " = " + result);
        //reste modulo
        result = nb1 % nb2;
        System.out.println(nb1 + " / " + nb2 + " = " + result);



    }

}
